    public boolean txt() {
        if (txt_nam.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống năm");
            return false;
        }
        if (!txt_nam.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Năm phải nhập là giá trị số");
            return false;
        }
        return true;
    }

