    public JPanel datase() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        try {
            for (int i = 1; i < 13; i++) {
                String sql = "Select sum(soluong*giaban)\n"
                        + "from DonHangChiTiet inner join SanPham on DonHangChiTiet.Masp=SanPham.Masp\n"
                        + "					inner join Donhang on DonHangChiTiet.MaDH=Donhang.MaDh\n"
                        + "where YEAR(Ngaymua)=" + Integer.parseInt(txt_nam.getText()) + "and MONTH(Ngaymua)=" + i;
                Connection conn = DBUtils.getConnection();
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                double a = 0;
                while (rs.next()) {
                    a = rs.getDouble(1);
                }
                dataset.setValue(a, "Doanh thu", "" + i);

            }
        } catch (Exception e) {
        }
        String title = "Biểu đồ doanh thu 12 tháng trong năm " + txt_nam.getText();
        JFreeChart chart = ChartFactory.createLineChart(title, "Tháng", "VNĐ", dataset);
        ChartPanel panel = new ChartPanel(chart);
        return panel;
    }

